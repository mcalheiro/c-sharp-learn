// Numbers

// Casting (type)var
int a = 2100000000;
int b = 2100000000;
long c = (long)a + (long)b;
Console.WriteLine(c);

// Floats
double e = 42.1; // Natural type
float f = 7.90F; // Explicit type
Console.WriteLine(e + f);

decimal g = 42.1M; // Natural type
decimal h = 7.90M; // Explicit type
Console.WriteLine(g + h);